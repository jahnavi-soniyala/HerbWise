import joblib
from flask import Flask, request, jsonify
from flask_cors import CORS
import pandas as pd
import pickle
app = Flask(__name__)
CORS(app)
def amazon_links(herbs):
    base_url="https://www.amazon.in/s?k="
    amazon_links=[]
    for i in herbs:
        i=i.replace(" ","").replace("_","")
        amazon_links.append(base_url+i);
    return amazon_links

@app.route('/')
def hello():
    symptoms = request.args.get('symptoms', '').split(',') 
    features = [ 1 if symptom in symptoms else 0 for symptom in SYMPTOMS]
    input=pd.DataFrame([features],columns=SYMPTOMS)
    model = joblib.load("miniproject.pkl")
    output=model.predict(input)
    disease=str(output).replace("[", "").replace("]", "").replace("'", "").strip()
    result ={
    'disease': disease,
    'remedies': remedies[disease]['practices'],
    'herbs':remedies[disease]['herbs'],
    'amazon_links': amazon_links(remedies[disease]['herbs'])
   }
    return jsonify(result)
if __name__ == "__main__":
    app.run(host='0.0.0.0', port=10000)

